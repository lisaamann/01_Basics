package at.la.cc.basics;

public class Rechenaufgabe {
    public static void main(String1[] args) {
        int neuezahl = 7;

        System.out.println(++neuezahl);
        System.out.println(neuezahl++);
        System.out.println(neuezahl=neuezahl*neuezahl);//81
        System.out.println(neuezahl);//81
        System.out.println(neuezahl=(neuezahl-9) / 8);
        System.out.println(neuezahl--);

        System.out.println(--neuezahl);
        System.out.println(neuezahl * neuezahl);


    }
}
